package pojo;


public class CustomerTable {
	private String customerId;
	private String firstName;
	private String lastName;
	private String englishName;
	private String gender;
	private String dob;
	private String email;
	private String cellPhone;
	private String homePhone;
	private String workPhone;
	private String comments;
	
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEnglishName() {
		return englishName;
	}
	public void setEnglishName(String englishName) {
		this.englishName = englishName;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getCellPhone() {
		return cellPhone;
	}
	public void setCellPhone(String cellPhone) {
		this.cellPhone = cellPhone;
	}
	public String getHomePhone() {
		return homePhone;
	}
	public void setHomePhone(String homePhone) {
		this.homePhone = homePhone;
	}
	public String getWorkPhone() {
		return workPhone;
	}
	public void setWorkPhone(String workPhone) {
		this.workPhone = workPhone;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	
	@Override
	public String toString() {
		return "CustomerTable [customerId=" + customerId + ", firstName=" + firstName + ", lastName=" + lastName
				+ ", englishName=" + englishName + ", gender=" + gender + ", dob=" + dob + ", email=" + email
				+ ", cellPhone=" + cellPhone + ", homePhone=" + homePhone + ", workPhone=" + workPhone + ", commets="
				+ comments + "]";
	}
	
}
